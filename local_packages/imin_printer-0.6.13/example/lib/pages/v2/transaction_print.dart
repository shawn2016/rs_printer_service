import 'package:flutter/material.dart';
import 'package:flutter/services.dart';
import 'package:imin_printer/imin_printer.dart';

class TransactionPrintPage extends StatefulWidget {
  const TransactionPrintPage({super.key});

  @override
  State<TransactionPrintPage> createState() => _TransactionPrintPageState();
}

class _TransactionPrintPageState extends State<TransactionPrintPage> {
  final iminPrinter = IminPrinter();
  bool firstTicket = false;
  bool secondTicket = false;
  bool thirdlyTicket = false;
  bool isOpenTransaction = false;
  List<int>? thirdlyTicketData;
  List<int>? secondTicketData;
  List<int>? firstTicketData;
  @override
  void initState() {
    super.initState();
  }

  @override
  void dispose() {
    debugPrint('dispose');
    iminPrinter.exitPrinterBuffer(true);
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: const Text('Transaction Print Page'),
      ),
      body: Column(
        children: [
          Row(
            children: [
              Expanded(
                child: Row(
                  mainAxisAlignment: MainAxisAlignment.center,
                  crossAxisAlignment: CrossAxisAlignment.center,
                  children: [
                    Checkbox(
                        value: firstTicket,
                        onChanged: (bool? value) async {
                          setState(() {
                            firstTicket = value!;
                          });
                          if (firstTicket) {
                            firstTicketData =
                              await getBaiduTestBytes();
                            if (!isOpenTransaction) {
                              await iminPrinter.sendRAWData(
                                  Uint8List.fromList(firstTicketData!));
                            }
                          } else {
                            firstTicketData = null;
                          }
                        }),
                    const Text('ticket 1')
                  ],
                ),
              ),
              Expanded(
                child: Row(
                  mainAxisAlignment: MainAxisAlignment.center,
                  crossAxisAlignment: CrossAxisAlignment.center,
                  children: [
                    Checkbox(
                        value: secondTicket,
                        onChanged: (bool? value) async {
                          setState(() {
                            secondTicket = value!;
                          });
                          if (secondTicket) {
                            secondTicketData = await getMeituanBill();
                            if (!isOpenTransaction) {
                              await iminPrinter.sendRAWData(
                                  Uint8List.fromList(secondTicketData!));
                            }
                          } else {
                            secondTicketData = null;
                          }
                        }),
                    const Text('ticket 2')
                  ],
                ),
              ),
              Expanded(
                child: Row(
                  mainAxisAlignment: MainAxisAlignment.center,
                  crossAxisAlignment: CrossAxisAlignment.center,
                  children: [
                    Checkbox(
                        value: thirdlyTicket,
                        onChanged: (bool? value) async {
                          setState(() {
                            thirdlyTicket = value!;
                          });
                          if (thirdlyTicket) {
                            thirdlyTicketData = await getErlmoData();
                            if (!isOpenTransaction) {
                              await iminPrinter.sendRAWData(
                                  Uint8List.fromList(thirdlyTicketData!));
                            }
                          } else {
                            thirdlyTicketData = null;
                          }
                        }),
                    const Text('ticket 3')
                  ],
                ),
              )
            ],
          ),
          ElevatedButton(
            onPressed: () {
              setState(() {
                isOpenTransaction = true;
              });
              iminPrinter.enterPrinterBuffer(true);
            },
            child: const Text('start transaction Print'),
          ),
          ElevatedButton(
            onPressed: () async {
              debugPrint('commit transaction');
              if (thirdlyTicketData != null) {
                await iminPrinter
                    .sendRAWData(Uint8List.fromList(thirdlyTicketData!));
              }
              if (secondTicketData != null) {
                await iminPrinter
                    .sendRAWData(Uint8List.fromList(secondTicketData!));
              }
              if (firstTicketData != null) {
                await iminPrinter
                    .sendRAWData(Uint8List.fromList(firstTicketData!));
              }
              await iminPrinter.commitPrinterBuffer();
            },
            child: const Text('commit transaction'),
          ),
        ],
      ),
    );
  }
}

